export function databaseAPI(table, query) {
  return new Promise((resolve, reject) => {
    if (![BENEFICIARY_TABLE, TRANSACTION_TABLE, BRANCH_TABLE].includes(table)) {
      simulateDelay(() => reject(null))
      return;
    }

    const count = query?.count;
    const ids = query?.ids;
    const columns = query?.columns;
    let tableArray;
    let result;

    if (table === BENEFICIARY_TABLE) tableArray = beneficiariesTable;
    else if (table === TRANSACTION_TABLE) tableArray = transactionsTable;
    else if (table === BRANCH_TABLE) tableArray = branchesTable;

    if(!tableArray) return simulateDelay(() => reject(null))

    result = tableArray;
    if (ids) result = tableArray.filter(row => ids.includes(row.id));
    if (count) result = result.slice(0, count);

    simulateDelay(() => resolve(result))
  });
}

function simulateDelay(callback, delay=2000){
  setTimeout(callback, delay)
}